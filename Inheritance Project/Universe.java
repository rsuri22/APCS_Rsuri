public class Universe {
    int size;
    int distance;

    public Universe(int size, int distance){
        this.size = size;
        this.distance = distance;
    }
    
    public int getSize() {
        return size;
    }

    public int getDistance() {
        return distance;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public void setDistance(int distance) {
        this.distance = distance;
    }




}
